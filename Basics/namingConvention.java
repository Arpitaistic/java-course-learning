public class namingConvention {
    public static void main(String[] args) {
        // This class demonstrates naming conventions in Java
        System.out.println("Naming conventions are important for code readability.");

        // Class names should start with an uppercase letter
        // Method names should start with a lowercase letter
        // Constants should be in uppercase letters

        String myVariable = "This is a variable";
        final int MY_CONSTANT = 100;

        System.out.println(myVariable);
        System.out.println("The constant value is: " + MY_CONSTANT);
        
        // Following naming conventions makes the code easier to understand and maintain.
    }
}
